import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");

const targetDir = process.argv[2];

if (!targetDir) {
	console.error("Usage: node scripts/generate-adhiraiyan-redirect-site.mjs <target-dir>");
	process.exit(1);
}

const redirectsPath = path.join(repoRoot, "migration", "adhiraiyan-redirects.json");
const redirects = JSON.parse(await fs.readFile(redirectsPath, "utf8"));

const readyRedirects = redirects.filter((entry) => entry.status === "ready");

function createRedirectHtml(destinationUrl) {
	return `<!doctype html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <meta name="robots" content="noindex" />
    <title>Redirecting...</title>
    <link rel="canonical" href="${destinationUrl}" />
    <meta http-equiv="refresh" content="0; url=${destinationUrl}" />
    <script>
      window.location.replace(${JSON.stringify(destinationUrl)});
    </script>
  </head>
  <body>
    <p>Redirecting to <a href="${destinationUrl}">${destinationUrl}</a>.</p>
  </body>
</html>
`;
}

function normalizeOldPath(oldPath) {
	if (oldPath === "/") return "";
	return oldPath.replace(/^\/+|\/+$/g, "");
}

function getOutputFiles(oldPath) {
	const normalized = normalizeOldPath(oldPath);

	if (!normalized) {
		return ["index.html"];
	}

	return [`${normalized}.html`, path.join(normalized, "index.html")];
}

async function ensureCleanTarget(dir) {
	await fs.mkdir(dir, { recursive: true });
	const existing = await fs.readdir(dir, { withFileTypes: true });

	for (const entry of existing) {
		const entryPath = path.join(dir, entry.name);
		await fs.rm(entryPath, { recursive: true, force: true });
	}
}

async function writeRedirectFiles(dir, entries) {
	for (const entry of entries) {
		const html = createRedirectHtml(entry.newUrl);
		const outputFiles = getOutputFiles(entry.oldPath);

		for (const relativeFile of outputFiles) {
			const fullPath = path.join(dir, relativeFile);
			await fs.mkdir(path.dirname(fullPath), { recursive: true });
			await fs.writeFile(fullPath, html, "utf8");
		}
	}
}

async function writeCname(dir) {
	await fs.writeFile(path.join(dir, "CNAME"), "www.adhiraiyan.org\n", "utf8");
}

async function writeReadme(dir, entries) {
	const lines = [
		"# Redirect-only Adhiraiyan site",
		"",
		"This directory was generated by `scripts/generate-adhiraiyan-redirect-site.mjs`.",
		"",
		"Every page here redirects to the corresponding page on `mukeshmithrakumar.com`.",
		"",
		"Generated redirects:",
		"",
		...entries.map((entry) => `- \`${entry.oldPath}\` -> \`${entry.newUrl}\``),
		"",
	];

	await fs.writeFile(path.join(dir, "README.md"), lines.join("\n"), "utf8");
}

await ensureCleanTarget(targetDir);
await writeRedirectFiles(targetDir, readyRedirects);
await writeCname(targetDir);
await writeReadme(targetDir, readyRedirects);

console.log(`Generated ${readyRedirects.length} redirect mappings in ${targetDir}`);
